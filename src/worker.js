for (const [key, values] of Object.entries(aliases)) {
	values.forEach(v => {

		jizdni_rad[v] = jizdni_rad[key].map(orig => {

			let idx = orig.zastavky.indexOf(key);
			orig.alias = key;

			if(idx >= 0) orig.zastavky[idx] = v;

			return orig;

		})

	});
}

